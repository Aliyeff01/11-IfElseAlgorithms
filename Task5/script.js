let leg1 = 3
let leg2 = 4
let hypotenuse = 5

let leg1Squared = leg1 * leg1
let leg2Squared = leg2 * leg2
let hypotenuseSquared = hypotenuse * hypotenuse

if (leg1Squared + leg2Squared === hypotenuseSquared) {
    console.log("Conclusion: it is a right triangle");
} else {
    console.log("Conclusion: it is not a right triangle");
}